    @Override
    public void actionPerformed(ActionEvent e) {
        var src = e.getSource();
        if (src.equals(this.btnCancel)) {
            dispose();
        }
        if (src.equals(this.btnAddImage)) {
            chooseImage();
        }
        if (src.equals(this.btnAdd)) {
            int c = 0;
            var name = txtName.getText().trim();
            var type = txtType.getText().trim();
            var quanaty = txtQuanati.getText().trim();
            var author = txtAuthor.getText().trim();
            var price = txtPrice.getText().trim();
            var dateComposition = txtDateComposition.getText();
            var IDSupplier = txtIDSupplier.getText().trim();
            var details = txtDetails.getText().trim();
            var imgSource = this.iconSting;
            if (check.isNumber(quanaty) && Integer.parseInt(quanaty) >= 0) {
                this.lbQuanity.setText(" ");
            } else {
                this.lbQuanity.setText("giá phải là 1 số >= 0");
            }
            if (check.isNumber(price) && Integer.parseInt(quanaty) >= 0) {
                this.lbPrice.setText("");
            } else {
                this.lbPrice.setText("giá phải là 1 số >= 0");
            }

            if (check.isNumber(quanaty) && check.isNumber(price) && Integer.parseInt(price) >= 0 && Integer.parseInt(quanaty) >= 0) {
                if (this.location == AddBookDialog.BTN_ADD) {
                    c = 0;
                    DTO.Book book = new Book(name, type, author, dateComposition,
                            price, this.iconSting, IDSupplier,
                            DAL.WriteDataToSQL.getDay(), quanaty, details);
                    System.out.println(DTO.Book.getCount());
                    DTO.upDateId.updateIDBook(book);
                    c = this.homeManage.AddBookCallBackHomeManage(book);
                    if (c == -1) {
                        dispose();
                        JOptionPane.showMessageDialog(rootPane, "Thêm Sách Thất Bại!");
                    } else {
                        dispose();
                        JOptionPane.showMessageDialog(rootPane, "Thêm Sách Thành Công!");
                    }
                }
                if (this.location == AddBookDialog.BTN_UPDATE) {
                    c = 0;
                    this.homeManage.getEditBook().edit(name, type, quanaty, author, price,
                            dateComposition, IDSupplier, details, this.iconSting);
                    System.out.println(iconSting);
                    c = DAL.EditDataToSQL.editDataBook(this.homeManage.getEditBook());
                    if (c != -1) {
                        this.homeManage.EditBookCallBackManage();
                        dispose();
                        JOptionPane.showMessageDialog(rootPane, "Sửa Sách Thành Công!");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Sửa Sách Thất Bại!");
                    }
                }
            }

        }
    }

