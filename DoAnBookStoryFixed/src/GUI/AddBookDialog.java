    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed

        if (txtName.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên sách muốn thêm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            DefaultListModel<String> dlm = new DefaultListModel<String>();

            jList1.setModel(dlm);
        } else {
            Book_BUS a = new Book_BUS();
            ArrayList<Book_DTO> arr = a.getBooksearch(txtName.getText().trim());
            
            if (arr.size() == 0) {
                int answer = JOptionPane.showConfirmDialog(null, "Sách này không có trong kho bạn có muốn thêm mới không?", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (answer == JOptionPane.YES_OPTION) {
                    btnAdd1.setVisible(false);
                    jLabel3.setVisible(true);
                    jLabel4.setVisible(true);
                    jLabel5.setVisible(true);
                    jLabel6.setVisible(true);
                    jLabel8.setVisible(true);
                    jLabel9.setVisible(true);
                    txtAuthor.setVisible(true);
                    txtType.setVisible(true);
                    txtPrice.setVisible(true);
                    txtQuanati.setVisible(true);
                    
                    txtDateComposition.setVisible(true);
                    txtIDSupplier.setVisible(true);
                    jPanel2.setVisible(true);
                    btnAdd.setVisible(true);
                    btnCancel.setVisible(true);
                }
            } else {
                DefaultListModel<String> dlm = new DefaultListModel<String>();
                int i = 0;
                for (Book_DTO x : arr) {
                    dlm.add(i, x.getName()+" - "+x.getID());
                    i++;
                }
                jList1.setModel(dlm);
            }
        }
    }//GEN-LAST:event_btnAdd1ActionPerformed

